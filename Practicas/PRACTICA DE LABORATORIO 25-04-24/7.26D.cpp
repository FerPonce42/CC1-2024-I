/*
Compare y contraste los enfoques aleatorios y exhaustivos de fuerza bruta en general.

El enfoque exhaustivo de fuerza bruta evalúa todas las posibles soluciones de manera sistemática,
el enfoque aleatorio genera soluciones de forma aleatoria y las evalúa, lo que puede ser más eficiente
en algunos casos pero no garantiza la óptima solución. La elección entre estos enfoques depende del problema 
específico y de los requisitos de eficiencia y precisión.


Exhaustivo de fuerza bruta:
Método: Evalúa sistemáticamente todas las posibles combinaciones o soluciones para encontrar la óptima.
Proceso: Recorre exhaustivamente cada opción posible, sin omitir ninguna, hasta encontrar la solución.

Aleatorio:
Método: Genera soluciones de forma aleatoria y las evalúa para encontrar una que cumpla con ciertos criterios.
Proceso: Produce soluciones de manera aleatoria y las evalúa, repitiendo el proceso varias veces
         hasta alcanzar una solución satisfactoria.
*/